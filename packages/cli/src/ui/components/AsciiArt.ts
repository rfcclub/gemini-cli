const isVestaEnv = () => {
  if (typeof process === 'undefined') {
    return false;
  }
  if (process.env?.['VITEST']) {
    return false;
  }
  return (
    process.env?.['VESTA_ATHANOR_DIR'] !== undefined ||
    process.env?.['ATHANOR_DIR'] !== undefined ||
    process.argv?.some((arg) => arg.includes('gemini-vesta') || arg.includes('gemini_vesta'))
  );
};

